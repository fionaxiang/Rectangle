
public class Rectangle{
	public static void main(String [] args){
		double height, width, Area, Perimeter;
		
		height = 3.5;
		width = 5;
		Area = height * width;
		Perimeter = 2*(height + width);
		
		System.out.println("The rectangle's area is " + Area + "\n" + "The rectangle's perimeter is " + Perimeter);
	}
	

}
